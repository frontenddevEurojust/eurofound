/**
 ** Adds a menu link translation with the specified title for the specified path and language and menu.
 ** Checks if the path is valid for the specified language, else points it to the respective default language (English) one.
 **/ 
function addMenuLinkTranslation($link_title, $path, $lang_code, $menu) {
  $DEFAULT_LANG_CODE = 'en';

  $source_system_path = drupal_lookup_path('source', $path, $DEFAULT_LANG_CODE);
  $source_system_path = $source_system_path == null || $source_system_path === '' ? $path : $source_system_path;
  $system_path = drupal_lookup_path('source', $path, $lang_code);
  if (!$system_path) {
    $system_path = $source_system_path;
  }

  echo "\npath:  " . $path . "\n";
  echo "source path:  " . $source_system_path . "\n";
  $source_item = getMenuLinkWithPathAndLanguage($source_system_path, $DEFAULT_LANG_CODE, $menu);
  echo "source item:  " . $source_item['mlid'] . "\n";

  $source_plid = $source_item['plid'];
  echo "source_plid:  " . $source_plid . "\n";
  if ($source_plid > 0) {
    $source_parent = menu_link_load($source_plid);
    $parent = i18n_menu_link_load($source_parent['link_path'], $lang_code);
    $plid = $parent != null ? $parent['mlid'] : 0;
  } else {
    $plid = 0;
  }
  echo "plid:  " . $plid . "\n";

  $menu_item = array(
    'link_title' => $link_title,
    'menu_name'  => $menu,
    'customized' => 1,
    'link_path'  => $system_path,
    'language'  => $lang_code,
    'plid'  => $plid,
    'weight'  => $source_item['weight'],
  );
  menu_link_save($menu_item);

  if (!empty($source_item['i18n_tsid'])) {
    $translation_set = i18n_translation_set_load($source_item['i18n_tsid']);
  }
  else {
    // Create object and stick the source information in the translation set.
    $translation_set = i18n_translation_set_build('menu_link')->add_item($source_item);
  }
  $translation_set->add_item($menu_item, $lang_code);
  $translation_set->save(TRUE);

  echo "finished:  " . $link_title;
}


function deleteMenuLinksOfLanguage($lang_code) {
  $result = db_query("SELECT * FROM {menu_links} WHERE language LIKE '" . $lang_code . "'");
  foreach ($result as $row) {
    $mlid = $row->mlid;
    echo("mlid: " . $mlid . "\n");
    menu_link_delete($mlid);
  }
}

/**
 * Grab a menu link item with the specified path and language
 */
function getMenuLinkWithPathAndLanguage($link_path, $lang_code, $menu) {
  $result = db_select('menu_links', 'm')
    ->fields('m', array('mlid'))
    ->condition('menu_name', $menu)
    ->condition('link_path', $link_path)
    ->condition('language', $lang_code)
    ->execute()
    ->fetchAssoc();
  return menu_link_load($result['mlid']);
}

